# while loop will continue executing until the condition becomes false
# In this  case it will print the value 0 infinite times until we stop the program
# Because the value of i will always be zero.
i=0
while(i==0):
    print (i)

# Now this will print 0 only 1 time as i value will increase to 1
# so the while condition becomes false
i=0
while(i==0):
    print (i)
    i=i+1